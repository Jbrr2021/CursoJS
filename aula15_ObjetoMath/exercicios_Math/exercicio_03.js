/*
 Crie uma função que receba um número como argumento e retorne o menor número inteiro maior ou igual ao número dado 
 unsando o método `Math.ceil()`
*/

function arredondarParaCima(numero) {
    return Math.ceil(numero);
}

console.log(arredondarParaCima(4.2));
console.log(arredondarParaCima(7.8));
console.log(arredondarParaCima(-2.3));

/*
 Crie uma função que receba um número com argumento e retorne a raiz quadrada de número unsando o método `Math.sqrt`
*/

function calcularRaizQuadrada(numero) {
    return Math.sqrt(numero)
}

console.log(calcularRaizQuadrada(16));
console.log(calcularRaizQuadrada(25));
console.log(calcularRaizQuadrada(9));